
function rz_banner() {
    local COLOR="\e[38;2;238;130;238m"
    local NC='\033[0m'

    # The ASCII art
    local art=(
        "##################################################"
        "##################################################"
        "##                                            ####"
        "##                                            ####"
        "##"
        "##          ####      ############  ########################"
        "##          ####  ################  ########################"
        "##          ######################  ######################"
        "##          ######################  ######################"
        "##          ##########                          ########"
        "##          ##########                        ########"
        "##          ########                          ########"
        "##          ########                        ########"
        "##          ########                        ########"
        "##          ########                      ########"
        "##          ########                    ########"
        "##          ########                    ######################"
        "##          ########                  ########################"
        "##          ########                  ########################"
        "##          ########                ##########################"
        "##"
        "##                                            ####"
        "##                                            ####"
        "##################################################"
    )


    local i=0
    for line in "${art[@]}"; do
        if [ $i -gt 4 ] && [ $i -lt 20 ]; then
            echo -n -e "${line:0:4}"
            echo -e "${COLOR}${line:5}${NC}"
        else
            echo "${line}"
        fi
        i=$(expr $i + 1)
    done
}
